type MoodGroup = 'angry' | 'normal' | 'happy';
function moodGroup(mood: EnemyMood): MoodGroup {
  if (mood === 'furious' || mood === 'upset') return 'angry';
  if (mood === 'favorable' || mood === 'devoted') return 'happy';
  return 'normal';
}

